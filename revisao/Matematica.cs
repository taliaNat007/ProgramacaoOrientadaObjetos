public class Matematica
{
    public double n1;
    public double n2;
    public double soma;
    public double multiplicacao;
    public double Soma()
    {
        soma = n1 + n2;
        return soma;
    }

    public double Multiplicar() 
    {
        multiplicacao = n1 * n2;
        return multiplicacao; 
    }
}
